package com.gitile.chinalife.entity;

/**
 * 车主信息
 */
public class OwnerInfo {
	
	private String ownerName;// 车主姓名
	private String ownerIdType;// 车主证件类型
	private String ownerIdNo;// 车主证件号码
	private String ownerGender;// 车主性别
	private String ownerBirthday;// 车主生日
	private String ownerAge;// 车主年龄
	private String ownerMobile;// 车主手机
	private String ownerEmail;// 车主邮箱
	private String ownerUserNick;// 车主淘宝Nick

	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getOwnerIdType() {
		return ownerIdType;
	}
	public void setOwnerIdType(String ownerIdType) {
		this.ownerIdType = ownerIdType;
	}
	public String getOwnerIdNo() {
		return ownerIdNo;
	}
	public void setOwnerIdNo(String ownerIdNo) {
		this.ownerIdNo = ownerIdNo;
	}
	public String getOwnerGender() {
		return ownerGender;
	}
	public void setOwnerGender(String ownerGender) {
		this.ownerGender = ownerGender;
	}
	public String getOwnerBirthday() {
		return ownerBirthday;
	}
	public void setOwnerBirthday(String ownerBirthday) {
		this.ownerBirthday = ownerBirthday;
	}
	public String getOwnerAge() {
		return ownerAge;
	}
	public void setOwnerAge(String ownerAge) {
		this.ownerAge = ownerAge;
	}
	public String getOwnerMobile() {
		return ownerMobile;
	}
	public void setOwnerMobile(String ownerMobile) {
		this.ownerMobile = ownerMobile;
	}
	public String getOwnerEmail() {
		return ownerEmail;
	}
	public void setOwnerEmail(String ownerEmail) {
		this.ownerEmail = ownerEmail;
	}
	public String getOwnerUserNick() {
		return ownerUserNick;
	}
	public void setOwnerUserNick(String ownerUserNick) {
		this.ownerUserNick = ownerUserNick;
	}

}